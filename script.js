const LANGUAGE = {
  current: localStorage.getItem('akay-lang') || 'tr',
  init() {
    this.apply(this.current);
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.langBtn === this.current);
      btn.addEventListener('click', () => this.switchTo(btn.dataset.langBtn));
    });
  },
  switchTo(lang) {
    if (lang === this.current) return;
    this.current = lang;
    localStorage.setItem('akay-lang', lang);
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.langBtn === lang);
    });
    this.apply(lang);
  },
  apply(lang) {
    const dict = translations[lang];
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (dict[key] !== undefined) el.textContent = dict[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder || el.dataset.i18n-placeholder;
      if (dict[key]) el.setAttribute('placeholder', dict[key]);
    });
  },
  t(key) { return translations[this.current][key] || ''; }
};

document.addEventListener('DOMContentLoaded', () => LANGUAGE.init());

// KASMA PROBEMİNİ ÇÖZEN HAFİF ARKA PLAN MOTORU
(() => {
  const canvas = document.getElementById('bg-canvas');
  if (!canvas || window.innerWidth < 768) return; // Mobilde işlemciyi yormamak için kapatır
  const ctx = canvas.getContext('2d');

  let width = window.innerWidth, height = window.innerHeight;
  let particles = [];

  function resize() {
    width = window.innerWidth; height = window.innerHeight;
    canvas.width = width; canvas.height = height;
    particles = [];
    // Sayı azaltıldı (Kasmayı engellemek için optimum değer)
    for (let i = 0; i < 40; i++) {
      particles.push({
        x: Math.random() * width, y: Math.random() * height,
        r: Math.random() * 1.5 + 0.5, speed: Math.random() * 0.2 + 0.1
      });
    }
  }

  function draw() {
    ctx.clearRect(0, 0, width, height);
    ctx.fillStyle = 'rgba(168, 85, 247, 0.3)';
    for (let p of particles) {
      p.y -= p.speed;
      if (p.y < 0) p.y = height;
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fill();
    }
    requestAnimationFrame(draw);
  }

  window.addEventListener('resize', resize);
  resize();
  draw();
})();

// İMLEÇ VE MOBİL ETKİLEŞİM
(() => {
  if (window.matchMedia('(hover: none)').matches) return;
  const ring = document.querySelector('.cursor-ring');
  const dot = document.querySelector('.cursor-dot');
  if (!ring || !dot) return;

  window.addEventListener('mousemove', (e) => {
    dot.style.transform = `translate3d(${e.clientX}px, ${e.clientY}px, 0)`;
    ring.style.transform = `translate3d(${e.clientX - 14}px, ${e.clientY - 14}px, 0)`;
  }, { passive: true });
})();

// SCROLL & MOBİL MENÜ SİSTEMİ
(() => {
  const nav = document.querySelector('.nav');
  window.addEventListener('scroll', () => {
    if (window.scrollY > 40) nav.classList.add('scrolled');
    else nav.classList.remove('scrolled');
  }, { passive: true });

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

  const toggle = document.querySelector('.nav-toggle');
  const links = document.querySelector('.nav-links');
  if (toggle && links) {
    toggle.addEventListener('click', () => links.classList.toggle('mobile-open'));
    links.querySelectorAll('a').forEach(a => a.addEventListener('click', () => links.classList.remove('mobile-open')));
  }
})();

// MODAL VE FORM DOĞRULAMA (E-POSTASIZ)
document.addEventListener('DOMContentLoaded', () => {
  const loader = document.getElementById('loader');
  if (loader) setTimeout(() => loader.classList.add('hidden'), 1500);

  const modal = document.getElementById('committee-modal');
  if (modal) {
    const modalTitle = modal.querySelector('.modal-title');
    const modalBody = modal.querySelector('.modal-body');

    document.querySelectorAll('[data-committee]').forEach(btn => {
      btn.addEventListener('click', () => {
        const key = btn.dataset.committee;
        modalTitle.textContent = LANGUAGE.t(`${key}Title`);
        modalBody.textContent = LANGUAGE.t(`${key}Detail`);
        modal.classList.add('active');
      });
    });
    modal.querySelector('.modal-close').addEventListener('click', () => modal.classList.remove('active'));
  }

  const form = document.getElementById('contact-form');
  if (form) {
    form.addEventListener('submit', (e) => {
      e.preventDefault();
      let valid = true;
      form.querySelectorAll('[data-required]').forEach(field => {
        const errorEl = field.parentElement.querySelector('.form-error');
        let ok = field.value.trim().length > 0;
        if (errorEl) errorEl.classList.toggle('show', !ok);
        if (!ok) valid = false;
      });
      if (valid) {
        form.style.display = 'none';
        document.getElementById('form-success').classList.add('show');
      }
    });
  }
});
